/// [AppRouterNames] defines constant route names used throughout the application.
/// 
/// These names are used to identify and reference routes in the navigation system.
/// 
/// Route Names:
/// - `home`: The main entry point of the app.
/// - `basic`: The screen for basic features.
/// - `requests`: The screen for handling requests.
/// - `shared`: The screen for shared features.
class AppRouterNames {
  static const String home = 'home';
  static const String basic = 'basic';
  static const String requests = 'requests';
  static const String shared = 'shared';
}
